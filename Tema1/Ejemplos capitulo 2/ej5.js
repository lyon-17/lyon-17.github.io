console.log("tipo de undefined: "+typeof undefined); // undefined
console.log("tipo de []: "+typeof []); // object
console.log("tipo de [1,2,3,4,5]: "+typeof [1,2,3,4,5]); //object
console.log("tipo de {}: "+typeof {}); //object
